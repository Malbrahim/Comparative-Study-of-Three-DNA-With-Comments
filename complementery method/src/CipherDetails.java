
public class CipherDetails {
    private String msg ;
    private String AMBIG;
    private String cipherBin;
    private String keyword;
    private String MEnglish;
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getAMBIG() {
		return AMBIG;
	}
	public void setAMBIG(String aMBIG) {
		AMBIG = aMBIG;
	}
	public String getCipherBin() {
		return cipherBin;
	}
	public void setCipherBin(String cipherBin) {
		this.cipherBin = cipherBin;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public String getMEnglish() {
		return MEnglish;
	}
	public void setMEnglish(String mEnglish) {
		MEnglish = mEnglish;
	}
}
